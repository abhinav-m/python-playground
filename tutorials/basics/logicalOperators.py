age = 5

# and keyword for and operator
if age > 3 and age < 6:
    print("Age between 3 and 6")

# or keyword for or operator
if age > 6 or age < 7:
    print("Age is less than 7 or greater than 6")

# not keyword for not operator
if not age >= 18:
    print("You are not an adult")
else:
    print("You are an adult")
